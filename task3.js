function checkValue3(key2, value2) {
    return key2.hobby === value2;
}

// 3) display friends yang hobby nya football
function checkHobby(x) { 
    return x.hobby.find(y => checkValue3(y, "football"));
}

function checkFootball(data) {
    let arr = [];
    for(let i = 0; i < data.length; i++) {
        let friends = data[i].friends;
        for(let j = 0; j < friends.length; j++) {
            if(checkHobby(friends[j])) {
                arr.push(friends[j]);
            }
        }
    }
    return arr
}
